// HELPER FUNCTIONS - Language-aware
// ============================================

// Get texts for current language
export const getBilincTexts = (lang = 'tr') => bilincTextsData[lang] || bilincTextsData.tr;
export const getFrekansTexts = (lang = 'tr') => frekansTextsData[lang] || frekansTextsData.tr;

// Legacy exports for backward compatibility (defaults to TR)
export const bilincTexts = bilincTextsData.tr;
export const frekansTexts = frekansTextsData.tr;

// Rastgele metin seçici (language-aware)
export const getRandomBilincText = (lang = 'tr') => {
  const texts = getBilincTexts(lang);
  return texts[Math.floor(Math.random() * texts.length)];
};

export const getRandomFrekansText = (lang = 'tr') => {
  const texts = getFrekansTexts(lang);
  return texts[Math.floor(Math.random() * texts.length)];
};

// Sıradaki metni getir (döngüsel, language-aware)
export const getNextText = (currentId, lang = 'tr', type = 'bilinc') => {
  const texts = type === 'bilinc' ? getBilincTexts(lang) : getFrekansTexts(lang);
  const currentIndex = texts.findIndex(t => t.id === currentId);
  const nextIndex = (currentIndex + 1) % texts.length;
  return texts[nextIndex];
};
